	private static final int NB_THREADS = 30;
	
	/**
	 * Each threads gets the same big list of dates, its own instance
	 * of unique date factory and we check if through all those threads
	 * all dates are really unique.
	 */
	@Test
	public void testUniqueDate1() {
		//Creating the Dates array
		Date[] dates = new Date[originalDatesStr.length];
		{
			int i=0;
			DateFormat df = new SimpleDateFormat("yyyy/MM/dd h:m:s");	//	2009/04/14 19:10:58
			for(String dateStr : originalDatesStr) {
				try {
					dates[i] = df.parse(dateStr);
				} catch (ParseException e) {
					assertTrue("Error not related to the unique date. It's just that we failed converting string to date", false);
				}
				i++;
			}
		}
		//Creating the object that will put together thread results
		UniqueDateTestThreadListener listener = new UniqueDateTestThreadListener();
		
		//Building uniquedatetes threads
		List<UniqueDateTestThread> threads = new ArrayList<UniqueDateFactoryTest.UniqueDateTestThread>(NB_THREADS);
		for(int i=0 ; i<NB_THREADS ; i++) {
			UniqueDateTestThread thread = new UniqueDateTestThread(listener);
			thread.setDates(dates);
			threads.add(thread);
		}
		
		//starting the threads
		for(UniqueDateTestThread thread : threads) {
			thread.start();
		}
		
		//waiting until they are all done
		for(UniqueDateTestThread thread : threads) {
			try {
				thread.join();
			} catch (InterruptedException e) {
				assertTrue("Error not related to the unique date. It's just that we failed waiting for the threads to be done", false);
			}
		}
		
		//retrieving result accumulated into the listener
		//And verifying weither dates are all unique or not
		List<Date> uniqueDates = listener.getUniqueDates();
		
		//Uncomment to creates a duplicate to make sure the test fails in case of duplicate
		//uniqueDates.add(uniqueDates.get(uniqueDates.size()-1));
		
		Set<Date> checkerForUniqueDates = new HashSet<Date>(uniqueDates);
		
		//Set have a unique constraint. So if there are duplicated dates,
		//the last one will overwrite the others, the set will have smaller
		//siz than the list
		assertTrue("Oups, we found a date that is not unique !!!!", 
				uniqueDates.size() == checkerForUniqueDates.size());
		
		Logger.getLogger(this.getClass().getName()).log(Level.FINE, "max offset in unique date creation = " + listener.getMaxOffset() + "ms");
	}
	
	
	
	private static class UniqueDateTestThread extends Thread {
		
		List<Date> dates = new ArrayList<Date>();
		UniqueDateTestThreadListener listener;
		
		public UniqueDateTestThread(UniqueDateTestThreadListener listener) {
			this.listener = listener;
		}
		
		public void setDates(Date[] dates) {
			Date[] temp = new Date[dates.length];
			System.arraycopy(dates, 0, temp, 0, dates.length);
			this.dates = Arrays.asList(temp);
		}
		
		public void run() {
			UniqueDateFactory uniqueDateFactory = new UniqueDateFactory();
			List<Date> uniqueDates = new ArrayList<Date>();
			for(Date date : dates) {
				Date uniqueDate = uniqueDateFactory.build(date);
				uniqueDates.add(uniqueDate);
			}
			listener.onFinished(uniqueDates, uniqueDateFactory.getMaxOffset());
		}
	}
	
	public static class UniqueDateTestThreadListener {
		
		List<Date> uniqueDates = Collections.synchronizedList(new ArrayList<Date>());
		int maxOffset = 0;
		
		public synchronized void onFinished(List<Date> generatedUniqueDates, int maxOffest) {
			uniqueDates.addAll(generatedUniqueDates);
			this.maxOffset = Math.max(maxOffest, this.maxOffset);
		}
		
		public List<Date> getUniqueDates() {
			return uniqueDates;
		}
		
		public int getMaxOffset() {
			return maxOffset;
		}
	}
	
